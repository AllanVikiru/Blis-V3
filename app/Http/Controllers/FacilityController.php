     */
    public function store(Request $request)
    {
        $rules = array('name' => 'required|unique:facilities,name');
        $validator = Validator::make(Input::all(), $rules);

        if ($validator->fails()) {
            return response()->json();
        } else {
            // Add
            $facility = new Facility;
            $facility->name = Input::get('name');
            $facility->save();
            // redirect
             return response()->json();
            
        }
    }

